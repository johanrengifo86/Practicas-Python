# Patrones regulares 
# sirven para componer una expresion regular
# <.> es un caracter
# <\n> nueva linea 
# <\r> retorno de carro
# <\t> tabulador horizontal
# <\w> cualquier caracter que representa un numero o letra minuscula
# <\W> cualquier caracter que representa un numero o letra en mayuscula
# <\s> engloba a todos los caracteres 
# <\S> cualquier caracter que no es un espacio en blanco
# <\d> es un numero entre 0-9
# <\D> cualquier caracter que no es un numero
# <­­­^> anuncia un inicio de cadena
# <$> fin de cadena
# <\> escape de caracteres especiales
# <[]> rango..cualquier caracter que se encuentre entre los corchetes
# <^[]> cualquier caracter que no se encuentra entre los corchetes
# <\b> separacion entre un numero o letra
#****Metacarácter****
# <+> numero de repeticiones una o más veces 
# <*> 0 o más veces 
# <?> 0 o una vez
# <{n}> numero de veces que se va a repetir